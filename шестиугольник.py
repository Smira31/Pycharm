import turtle as t
t.shape('arrow')
for i in range(2):
    t.color('yellow')
    t.forward(100)
    t.left(360 / 8)
    t.color('green')
    t.forward(100)
    t.left(360 / 8)
    t.color('red')
    t.forward(100)
    t.left(360 / 8)
    t.color('blue')
    t.forward(100)
    t.left(360 / 8)
    t.color('black')




t.done()









